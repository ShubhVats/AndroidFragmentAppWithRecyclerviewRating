package com.example.assignment.models;

public class Contact {
    private String phone;
    private String formattedPhone;
    private String twitter;
    private String instagram;
    private String facebook;
    private String facebookUsername;
    private String facebookName;

    public String getPhone() {
        return phone;
    }

    public void setPhone(String value) {
        this.phone = value;
    }

    public String getFormattedPhone() {
        return formattedPhone;
    }

    public void setFormattedPhone(String value) {
        this.formattedPhone = value;
    }

    public String getTwitter() {
        return twitter;
    }

    public void setTwitter(String value) {
        this.twitter = value;
    }

    public String getInstagram() {
        return instagram;
    }

    public void setInstagram(String value) {
        this.instagram = value;
    }

    public String getFacebook() {
        return facebook;
    }

    public void setFacebook(String value) {
        this.facebook = value;
    }

    public String getFacebookUsername() {
        return facebookUsername;
    }

    public void setFacebookUsername(String value) {
        this.facebookUsername = value;
    }

    public String getFacebookName() {
        return facebookName;
    }

    public void setFacebookName(String value) {
        this.facebookName = value;
    }
}
